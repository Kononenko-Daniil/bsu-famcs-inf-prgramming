/**
 * A program to print three lines of a Robert Frost poem
 */

public class Frost {
  public static void main(String[] args) {
    System.out.println("Two roads diverged in a wood, and I --\n" +
        "I took the one less traveled by,\n" +
        "And that has made all the difference.\n" +
        "    by Robert Frost");
  }
}